package Week9ArraysInJava.Class9point6ArraysWithLoops;

public class VideoPt2 {
    public static void main(String[] args) {
        String[] cities = {"San Francisco", "San Jose", "LA", "LV", "San Diego", "Tempe", "Baltimore"};

        for(int i = 0; i<cities.length; i++) {
            System.out.println(cities[i]); // prints out every city
        }
    }
}
