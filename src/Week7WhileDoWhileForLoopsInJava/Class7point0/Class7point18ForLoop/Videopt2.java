package Week7WhileDoWhileForLoopsInJava.Class7point0.Class7point18ForLoop;

//write a program that prints out numbers from 11 - -5
public class Videopt2 {

    public static void main(String[] args) {

        for (int counter = 11; counter >= -5; counter--){
            System.out.println(counter); //prints from 11 till -5
        }
    }
}
