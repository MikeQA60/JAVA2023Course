package Week7WhileDoWhileForLoopsInJava.Class7point0.Class7point3WhileLoopPrintNumbers;

public class JobOfferfromAirbnb {
    public static void main(String[] args) {

        int counter = 1; //starting point
        while(counter <= 6) { //ending point

            //prints out 6 times going from 1-6
            System.out.println("$150k/year Job Offer Loading... at wedevx month " +counter);
            counter++;
        }

        //last portion
        System.out.println("Offer from Airbnb Received!");
    }
}
