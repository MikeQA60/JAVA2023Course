package Week7WhileDoWhileForLoopsInJava.Class7point0.Class7point2WhileLoop;

public class IhaveaDreamAtApple {
    public static void main(String[] args) {
        int counter2 = 1; //starting point
        while (counter2 <= 5) { //ending point
            System.out.println("I have a dream... \n" +
                    "to become a tech lead at Apple");
            counter2++; //updater
        }
    }
}
